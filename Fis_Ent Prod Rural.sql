SELECT X.NROEMPRESA LOJA, X.NUMERONF NOTA, TO_CHAR(DTAEMISSAO, 'DD/MM/YYYY') DTAEMISSAO, CODGERALOPER CGO, FVALORTOTALNF(X.ROWID, 'N') VALORNF, 
       (SELECT SUM(NVL(XI.VLRFUNRURALITEM,0)) FROM MLF_NFITEM XI WHERE XI.SEQNF = X.SEQNF) VALOR_FUNRURAL,
       X.SEQPESSOA||' - '||G.NOMERAZAO FORNEC 
  FROM MLF_NOTAFISCAL X INNER JOIN GE_PESSOA G ON G.SEQPESSOA = X.SEQPESSOA
 WHERE X.CODGERALOPER IN (12,19,81,82,83,84,85,104,200,203,204,254,255,853)
   AND X.DTAEMISSAO BETWEEN DATE '2026-04-01' AND DATE '2026-04-09'
