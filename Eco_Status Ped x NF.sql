SELECT X.NROEMPRESA, NUMERODF, Z.NROPEDIDOAFV, Z.NROPEDVENDA, X.SEQPESSOA, X.STATUSDF, Z.STATUSPEDIDO
  FROM MFL_DOCTOFISCAL X 
  INNER JOIN CONSINCO.EDI_PEDVENDA Z ON X.NROPEDIDOVENDA = Z.NROPEDVENDA AND X.NROEMPRESA = Z.NROEMPRESA AND X.SEQPESSOA = Z.SEQPESSOA
WHERE Z.NROPEDIDOAFV IN (1176405522,78992741,79263424,79504606,79458495,79695692,80266466,80787980,80862241)
