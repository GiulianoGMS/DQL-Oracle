ALTER SESSION SET current_schema = CONSINCO;

SELECT DISTINCT F.CODESPECIE, F.NROEMPRESA, F.NROTITULO, F.SEQPESSOA||'-'||G.NOMERAZAO FORNECEDOR, C.VLRDSCFINANC, F.DTAINCLUSAO, F.DTAVENCIMENTO

FROM FI_TITULO F LEFT JOIN FI_COMPLTITULO C ON F.SEQTITULO = C.SEQTITULO 
                 LEFT JOIN GE_PESSOA G ON F.SEQPESSOA = G.SEQPESSOA

WHERE C.VLRDSCFINANC > 0
AND F.ABERTOQUITADO = 'A'
AND NROEMPRESA NOT IN (501,502,503,504,505,506)
AND F.SEQPESSOA IN (1039,114645,114827,1404,134448,1038,127074,120003,134371,134373,113927,128852,119287,131079,115857,
                   116696,115975,418235,473115,941126,389193,986373,470904,1379443)
                    
AND F.DTAVENCIMENTO >= SYSDATE
AND F.SEQTITULO NOT IN (SELECT FM.SEQIDENTIFICA FROM FI_MOVOCOR FM WHERE Fm.CODOCORRENCIA IN(68,78,23) AND FM.SEQPESSOA = F.SEQPESSOA)

ORDER BY 2
