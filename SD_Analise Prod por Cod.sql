SELECT DISTINCT
       'Loja: ' || E.NOMEREDUZIDO AS LOJA,
       'Cod: ' || :NR1 ||
       CASE
         WHEN NOT EXISTS (
              SELECT 1
                FROM MAP_PRODCODIGO C
               WHERE (C.TIPCODIGO = 'E' AND C.CODACESSO = :NR1)
                  OR C.SEQPRODUTO = :NR1
         )
         THEN ' Não Cadastrado!'
         ELSE ' - ' ||
              (SELECT A.DESCCOMPLETA
                 FROM MAP_PRODUTO A
                WHERE A.SEQPRODUTO =
                      (SELECT C.SEQPRODUTO
                         FROM MAP_PRODCODIGO C
                        WHERE ((C.TIPCODIGO = 'E' AND C.CODACESSO = :NR1)
                               OR C.SEQPRODUTO = :NR1)
                          AND ROWNUM = 1))
       END AS INFO
FROM MAX_EMPRESA E
WHERE E.NROEMPRESA = #LS1

