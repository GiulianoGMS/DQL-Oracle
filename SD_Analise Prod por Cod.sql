SELECT
       'Última Nota Recebida:',
       X.NUMERONF || ' - ' || TO_CHAR(X.DTARECEBIMENTO,'DD/MM/YYYY')
FROM (
    SELECT DISTINCT
           NF.NUMERONF,
           NVL(NF.DTARECEBIMENTO, NF.DTAENTRADA) AS DTARECEBIMENTO
    FROM MLF_NOTAFISCAL NF
    INNER JOIN MLF_NFITEM NI
            ON NI.NROEMPRESA     = NF.NROEMPRESA
           AND NI.NUMERONF      = NF.NUMERONF
           AND NI.SERIENF       = NF.SERIENF
           AND NI.TIPNOTAFISCAL = NF.TIPNOTAFISCAL
    INNER JOIN MAP_PRODCODIGO C
            ON C.SEQPRODUTO = NI.SEQPRODUTO
    WHERE NF.NROEMPRESA = #LS1
      AND ((C.TIPCODIGO = 'E' AND C.CODACESSO = :NR1)
           OR C.SEQPRODUTO = :NR1)
      AND NVL(NF.DTARECEBIMENTO, NF.DTAENTRADA) IS NOT NULL
    ORDER BY NVL(NF.DTARECEBIMENTO, NF.DTAENTRADA) DESC,
             NF.NUMERONF DESC
) X
WHERE ROWNUM = 1;
