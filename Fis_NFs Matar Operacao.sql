SELECT NUMERONF, SEQPESSOA, NROEMPRESA, CODGERALOPER, DTAEMISSAO,
       SUBSTR(REGEXP_SUBSTR(OBSERVACAO, ':[^:](\S*)'),3,10) NF_REFERENCIADA, A.TIPNOTAFISCAL
      
 FROM MLF_NOTAFISCAL A 
WHERE A.CODGERALOPER IN (202,16,69,135,22,141,12,169,135,27,23,26) 
  AND A.DTAEMISSAO > SYSDATE - 50
  AND SEQPESSOA < 999
 
ORDER BY DTAEMISSAO DESC;
